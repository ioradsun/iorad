
-- Create iorad_libraries table for AI tool matching
CREATE TABLE public.iorad_libraries (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  label TEXT NOT NULL UNIQUE,
  help_center_url TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

ALTER TABLE public.iorad_libraries ENABLE ROW LEVEL SECURITY;

-- Public read access (edge functions use service role anyway)
CREATE POLICY "Anyone can read libraries" ON public.iorad_libraries FOR SELECT USING (true);

-- Only admins can modify
CREATE POLICY "Admins can manage libraries" ON public.iorad_libraries FOR ALL USING (
  EXISTS (SELECT 1 FROM public.user_roles WHERE user_id = auth.uid() AND role = 'admin')
);

